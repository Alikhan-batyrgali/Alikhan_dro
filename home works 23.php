<?php

$товары = array(
    "Кока Кола" => 5,
    "Хлеб" => 15,
    "Батон" => 8,
    "Шоколад" => 3,
    "Мороженное" => 12
);

$недостаточное_количество = array_filter($товары, function($количество) {
    return $количество < 10;
});

echo "Товары с количеством меньше 10 штук:\n <br />";
foreach ($недостаточное_количество as $товар => $количество) {
    echo $товар . ": " . $количество . " шт.\n <br />";
}

?>