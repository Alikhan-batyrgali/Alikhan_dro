<?php
$a = 100; // Сумма для конвертации
$b = "доллар"; // Выбор валюты (доллар, евро, фунт)

switch ($b) {
    case "доллар":
        $c = $a * 1.09; // Курс доллара к евро
        echo "Сумма $a евро эквивалентна $c долларам.";
        break;
    case "евро":
        $c = $a * 0.92; // Курс евро к доллару
        echo "Сумма $a долларов эквивалентна $c евро.";
        break;
    case "фунт":
        $c = $a * 0.78; // Курс фунта к доллару
        echo "Сумма $a долларов эквивалентна $c фунтам.";
        break;
    default:
        echo "Выбрана неверная валюта. Пожалуйста, выберите из доллара, евро или фунта.";
}
?>